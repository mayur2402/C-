class Bitwise
{
	public static void Main()
	{
		byte a=3;
		byte b=5;
		System.Console.Write("\n(a|b)"+(a|b));
		System.Console.Write("\n(a&b)"+(a&b));
		System.Console.Write("\n(~a|b)"+(~a|b));
		System.Console.Write("\n(a|~b)"+(a|~b));
		System.Console.Write("\n(~a&b)"+(~a&b));
		System.Console.Write("\n(a&~b)"+(a&~b));
		System.Console.Write("\n(a^b)"+(a^b));
		System.Console.Write("\n(a<b)"+(a<b));
		System.Console.Write("\n(a>b)"+(a>b));
		System.Console.Write("\n(a==b)"+(a==b));
		System.Console.Write("\n(a!=b)"+(a!=b));
		
	}
}
