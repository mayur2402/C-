class Arithmatics
{
	public static void Main()
	{
		int a1=10;
		double b=a1/4.0;
		System.Console.Write(b);
		int a2=11;
		System.Console.Write("\n"+(a1+a2));
		System.Console.Write("\n"+(a1+(a2++)));
		System.Console.Write("\n"+(a1+a2));
		System.Console.Write("\n"+(a1+(++a2)));
		System.Console.Write("\n"+(a1+a2));
		int a3=20;
		double a4=0;
		System.Console.Write("\n"+a3/a4);
	}
}
