class Datatype1
{
	public static void Main()
	{
		float f=3.142f;
		System.Console.Write("\nFloat is "+f);
		double d=3.142;
		System.Console.Write("\nDouble is "+d);
		double d1=double.NaN;
		System.Console.Write("\nInvalid number "+d1);
		double d2=double.PositiveInfinity;
		System.Console.Write("\nPositive Infinity number "+d2);
		double d3=double.NegativeInfinity;
		System.Console.Write("\nNegative Infinity number "+d3);
	}
}
